package model;

/**
 *
 * @author pvkha
 */
public class TaiSan {
    int id;
    String tenTaiSan;
    String moTa;

    public TaiSan() {
    }

    public TaiSan(int id, String tenTaiSan, String moTa) {
        this.id = id;
        this.tenTaiSan = tenTaiSan;
        this.moTa = moTa;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenTaiSan() {
        return tenTaiSan;
    }

    public void setTenTaiSan(String tenTaiSan) {
        this.tenTaiSan = tenTaiSan;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
    
    
}
